package oop1;

public class Product {
	private String name;
	private double unitPrice;
	private double discort;
	private String imageUr1;
	private int unitInStock;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(double unitPrice) {
		this.unitPrice = unitPrice;
	}
	public double getDiscort() {
		return discort;
	}
	public void setDiscort(double discort) {
		this.discort = discort;
	}
	public String getImageUr1() {
		return imageUr1;
	}
	public void setImageUr1(String imageUr1) {
		this.imageUr1 = imageUr1;
	}
	public int getUnitInStock() {
		return unitInStock;
	}
	public void setUnitInStock(int unitInStock) {
		this.unitInStock = unitInStock;
	}
	
	
	
	
	
	
	
	
}
