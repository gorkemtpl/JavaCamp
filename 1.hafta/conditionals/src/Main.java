public class Main {
    public static void main(String[] args) {
        /*
        Bir Şart Bloğu Oluşturmak İçin 
        1. if - else if - else Koşulu kullanılır.
        
        */
        
        
        int sayi = 9;
        if (sayi < 20){//Eğer Sayı 20'den Küçükse (doğruysa) Bu Kod Bloğunu Çalıştır.
            System.out.println("Sayımız 20'den Küçüktür...");
        }
        else {//Şart Bloğunun Düşünda Bir Durum Olursa Bu Kod Bloğunu Çalıştır.
            System.out.println("Sayı 20'den Küçük Değildir...");
            
        }
        
        int a = 100;
        if (a > 99){// a 99'dan Büyükse Bu Kod Bloğunu Çalıştır.
            System.out.println("Sayımız 99'dan Büyüktür...");
        }
        else {// a 99'dan Büyük Değilse Bu Kod Bloğunu Çalıştır.
            System.out.println("Sayımız 99'dan Büyük Değildir...");
            
        }
        
}
}
